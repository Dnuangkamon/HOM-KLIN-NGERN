    public void executeSQLQuery(String query, String Message){
        con = MyConnection.getConnection(); //‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏° database
        try{
            st = con.createStatement();
            if(st.executeUpdate(query) != 0){
                //refresh JTable
                DefaultTableModel model = (DefaultTableModel)jTable_Menu.getModel();
                model.setRowCount(0);
                Show_Menu_IN_JTable();
                
                JOptionPane.showMessageDialog(null, "Data "+Message+" Success");
            }else{
                JOptionPane.showMessageDialog(null, "Data not "+Message);
            }
        } catch (HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(null, "Data not "+e);
        }
    }

