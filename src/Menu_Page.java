    public ArrayList<Menu> getMenuList(){
        ArrayList<Menu> menuList = new ArrayList<Menu>();
        con = MyConnection.getConnection(); //‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏° database
        String query = "select * from menu"; //‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏ï‡∏≤‡∏£‡∏≤‡∏á menu
        
        try{
            st = con.createStatement();
            rs = st.executeQuery(query);
            Menu menu;
            while(rs.next()){
                menu = new Menu(rs.getString("username_menu"), rs.getString("category_menu"), rs.getString("name"), rs.getInt("price"));
                System.out.println("run menu");
//                user = new User(rs.getInt("ID"),rs.getString("Name"),rs.getString("Surname"),rs.getInt("Age"));
                menuList.add(menu);
            }
        } catch (Exception e){
            e.printStackTrace();
        }
        return menuList;
    }
    
    //Display Data in Table
    
    public void Show_Menu_IN_JTable(){
        
        ArrayList<Menu> list = getMenuList();
        DefaultTableModel model = (DefaultTableModel)jTable_Menu.getModel();
        Object[] row = new Object[2];
        for(int i=0; i<list.size();i++){
            row[0] = list.get(i).getName();
            row[1] = list.get(i).getPrice();
            
            model.addRow(row);
        }
    }
    
